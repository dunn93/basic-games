# a dictionary allows you to save key value pairs
# key value pairs = a key and it's responding value
# like how a normal dictionary has a word that corresponds with a meaning
# keys have to be unique
# keys can be string and ints

monthConversions = {
    "Jan": "January",
    "Feb": "February",
    "Mar": "March"
}

print(monthConversions.get("int", "Not a valid key"))